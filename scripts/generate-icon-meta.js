#!/usr/bin/env node
/**
 * Generate lightweight icon metadata for the demo website.
 * This produces src/icons/icon-meta.js containing only names/slugs/variant info.
 * No actual React components are imported, keeping the initial bundle tiny.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const COMPONENTS_DIR = path.join(__dirname, "../src/icons/components");
const OUTPUT_PATH = path.join(__dirname, "../src/icons/icon-meta.js");

console.log("🔍 Scanning icon components for metadata...");

const dirs = fs
  .readdirSync(COMPONENTS_DIR, { withFileTypes: true })
  .filter((dirent) => dirent.isDirectory())
  .map((dirent) => dirent.name)
  .sort((a, b) => a.localeCompare(b));

const iconMeta = dirs.map((slug) => {
  // Derive a human name
  const name = slug
    .split("-")
    .map((part) => {
      // Handle leading numbers like "3d", "24"
      if (/^\d/.test(part)) return part;
      return part.charAt(0).toUpperCase() + part.slice(1);
    })
    .join(" ")
    .trim();

  // Standard variants for this icon set
  const variants = ["bold", "broken", "bulk", "linear", "outline", "twotone"];

  return {
    name: name || slug,
    slug,
    variants,
  };
});

const fileContent = `// AUTO-GENERATED by scripts/generate-icon-meta.js
// Lightweight metadata only — no component implementations.
// Used by the demo website for fast initial load.

export const iconMeta = ${JSON.stringify(iconMeta, null, 2)};
`;

fs.writeFileSync(OUTPUT_PATH, fileContent, "utf-8");

console.log(`✅ Generated icon metadata for ${iconMeta.length} icons`);
console.log(`   → ${OUTPUT_PATH}`);
console.log("   The demo will now lazy-load icon components on demand.");
